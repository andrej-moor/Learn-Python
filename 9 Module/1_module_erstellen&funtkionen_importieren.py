# Über import können Funktionen aus anderen Python-Dateien importiert werden
# Man lager Code quasi aus
# Zum Beispiel -> import file_name
# Beim Einsatz der Funktion hängt man den Funktionnamen mit einem . an -> file_name.function_name()

# Alternativ kann man die Funktion auch direkt importieren mit -> from file_name import function_name

# Aliases einfüfen -> from file_name import function_name as function_name_alias

# Alle Funtkionen importieren -> from file_name import *