# Variablen sind Zwischenspeicher für Werte
# Im Arbeitsspeicher werden Bytes für die Variablen reserviert

first_name = "Andreas"
last_name = "Moor"
# first_name ist der Variablen Name
# "Andreas" ist der Wert als Datentyp String

print(f"Hallo {first_name} {last_name}!")
# Das f steht für eine "formated String" 

print("Hallo {first_name} {last_name}!")
# Ohne das f werden beide Variable in Klammern mit dem Variablennamen anstatt dem Wert der Variable ausgegeben.


