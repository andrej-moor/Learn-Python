# Ein Dictionarie besteht immer aus zwei Key-Value-Pairs, von denen es aber mehrere geben kann
            
person = {"name" : "Jannick", "height" : 180}

print(person["name"])
print(person["height"])

print(f"Name ist {person['name']} und körpergröße in cm ist {person['height']}") # Verwendung von einfachen Anführungszeichen '' das sonst die String "Name ist {person[" interpretiert wird

